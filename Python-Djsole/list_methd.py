# list of append method 
fruits = ["apple","banana","mango"]
fruits.append("grapes")
print(fruits)
# list of clear method
animals = ["cat","dog","goat"]
animals.clear()
print(animals)
# list of copy method
cars = ['honda','mehran',"BMW","Alto"]
cars.copy()
print(cars)
# list of count method
subjects = ["math","english","urdu","computer","english"]
x = subjects.count("english")
print(x)

# list of extend method
list1 = [20,23,34,45,56]
list2 = [100,200,300]
list1.extend(list2)
print(list1)
# list of index method
city = ["lahore","islamabad","sialkot","pashawer"]
city.index('islamabad')
print(city)

# list of insert method
country = ["pakistan","india","corea","japan"]
country.insert(2,"america")
print(country)
# pop in list method

vegetables = ["potato","onions","cocumber"]
vegetables.pop(1)
print(vegetables)
# remove in list method
city = ["lahore","karachi","islamabad","peshawer"]
city.remove("lahore")
print(city)

# reverse method
numbers = [10,20,30,40,50]
numbers.reverse()
print(numbers)

# sorts method
cars = ["sords","BMW","volvo"]
cars.sort()
print(cars)